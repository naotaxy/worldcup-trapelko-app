// Best-effort katakana names for every squad player, sourced from Wikidata
// Japanese labels. No football API provides katakana, and not every fringe
// player has a ja label, so this is intentionally partial: famous players come
// out accurate, the rest stay in English. Precision over coverage — we only
// accept an entity whose English description marks it as a footballer, to avoid
// homonym mistakes (the actor vs the player).
//
//   node scripts/build-player-katakana.mjs
//
// Writes src/data/playerNamesJa.ts (committed; no runtime Wikidata dependency).
import { writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const here = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(here, '..')
const { squads } = await import(path.join(root, 'src', 'data', 'squads.ts'))

const UA = 'wc2026-trapelko-katakana/1.0 (https://github.com/naotaxy/worldcup-trapelko-app)'
const FOOTBALL = /foot|soccer/i

const names = [...new Set(Object.values(squads).flatMap((list) => list.map((p) => p.name)))]
console.log(`unique players: ${names.length}`)

const sleep = (ms) => new Promise((r) => setTimeout(r, ms))

async function getJson(url) {
  for (let attempt = 0; attempt < 3; attempt += 1) {
    try {
      const res = await fetch(url, { headers: { 'user-agent': UA, accept: 'application/json' } })
      if (res.status === 429) {
        await sleep(1500 * (attempt + 1))
        continue
      }
      if (!res.ok) return null
      return await res.json()
    } catch {
      await sleep(500)
    }
  }
  return null
}

// Step 1: resolve each name to a footballer Wikidata id (top matching result
// whose English description mentions football/soccer).
async function resolveId(name) {
  const url =
    `https://www.wikidata.org/w/api.php?action=wbsearchentities&format=json&language=en&uselang=en&type=item&limit=6&search=` +
    encodeURIComponent(name)
  const data = await getJson(url)
  const hits = data?.search || []
  const football = hits.find((h) => FOOTBALL.test(h.description || ''))
  return football ? football.id : null
}

const idByName = {}
const concurrency = 6
let cursor = 0
let resolved = 0
async function worker() {
  while (cursor < names.length) {
    const i = cursor++
    const name = names[i]
    const id = await resolveId(name)
    if (id) {
      idByName[name] = id
      resolved += 1
    }
    if (i % 100 === 0) console.log(`  search ${i}/${names.length} (matched footballer: ${resolved})`)
    await sleep(40)
  }
}
await Promise.all(Array.from({ length: concurrency }, worker))
console.log(`resolved footballer entities: ${resolved}`)

// Step 2: batch-fetch ja labels (50 ids per call).
const entries = Object.entries(idByName)
const jaByName = {}
for (let i = 0; i < entries.length; i += 50) {
  const batch = entries.slice(i, i + 50)
  const ids = batch.map(([, id]) => id).join('|')
  const url = `https://www.wikidata.org/w/api.php?action=wbgetentities&format=json&props=labels&languages=ja&ids=${ids}`
  const data = await getJson(url)
  const ents = data?.entities || {}
  for (const [name, id] of batch) {
    const ja = ents[id]?.labels?.ja?.value
    if (ja) jaByName[name] = ja
  }
  console.log(`  labels ${Math.min(i + 50, entries.length)}/${entries.length} (ja so far: ${Object.keys(jaByName).length})`)
  await sleep(120)
}

const sorted = Object.keys(jaByName)
  .sort((a, b) => a.localeCompare(b))
  .reduce((acc, key) => ({ ...acc, [key]: jaByName[key] }), {})

const ts =
  `// AUTO-GENERATED by scripts/build-player-katakana.mjs from Wikidata ja labels.\n` +
  `// Partial by design: players without a Wikidata ja label keep their English name.\n` +
  `// Coverage: ${Object.keys(sorted).length}/${names.length} players.\n` +
  `export const playerNamesJa: Record<string, string> = ${JSON.stringify(sorted, null, 2)}\n`

await writeFile(path.join(root, 'src', 'data', 'playerNamesJa.ts'), ts, 'utf8')
console.log(`\nDONE. katakana for ${Object.keys(sorted).length}/${names.length} players -> src/data/playerNamesJa.ts`)
