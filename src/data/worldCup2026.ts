function buildGroupFixtures(group: Group, groupIndex: number): Match[] {
  const [a, b, c, d] = group.teams
  const pairings: Array<[Team, Team]> = [
    [a, b],
    [c, d],
    [a, c],
    [d, b],
    [d, a],
    [b, c],
  ]

  return pairings.map(([home, away], index) => ({
    id: `${group.code}-${index + 1}`,
    group: group.code,
    date: fixtureDates[(groupIndex + index * 3) % fixtureDates.length],
    venue: 'Official schedule slot',
    homeTeamId: home.id,
    awayTeamId: away.id,
    result: { home: null, away: null },
    highlightUrl: `https://www.youtube.com/results?search_query=FIFA+World+Cup+2026+${home.name}+${away.name}+highlights`,
    newsUrl: `https://news.yahoo.co.jp/search?p=${encodeURIComponent(`${home.name} ${away.name} World Cup 2026`)}`,
    featuredPlayers: [],
  }))
}

export const fixtures: Match[] = groups.flatMap((group, index) => buildGroupFixtures(group, index))

